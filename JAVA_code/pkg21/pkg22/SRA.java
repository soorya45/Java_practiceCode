package pkg21.pkg22;

public class SRA {
    private String sraName;
    private String sraID;

    public SRA(String sraName, String sraID) {
        this.sraName = sraName;
        this.sraID = sraID;
    }

    public String getSraName() {
        return sraName;
    }

    public String getSraID() {
        return sraID;
    }
}